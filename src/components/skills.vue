    <script>
    export default {
      name: 'skills_page',
      mounted() {
        // Get all elements that need to be animated
        const frontElement = document.querySelector('.front');
        const backElement = document.querySelector('.back');
        const devOpsElement = document.querySelector('.dev_ops');
    
        // Function to check if an element is at least half in the viewport
        function isHalfInViewport(element) {
          const rect = element.getBoundingClientRect();
          console.log(rect.bottom,"//",rect.height,">>>",window.innerHeight)
          return (
             (rect.bottom >= 0 && (rect.bottom-rect.height/1.2) <= window.innerHeight)
          );
        }
    
        // Function to handle scroll event
        function handle_scroll() {
          if (isHalfInViewport(frontElement)) {
            console.log("is halfe visible >>>");
        frontElement.classList.add('slide-left');
      } else {
        frontElement.classList.remove('slide-left');
      }
    
          if (isHalfInViewport(backElement)) {
            backElement.classList.add('slide-right');
          } else {
            backElement.classList.remove('slide-right');
          }
    
          if (isHalfInViewport(devOpsElement)) {
            devOpsElement.classList.add('slide-up');
          } else {
            devOpsElement.classList.remove('slide-up');
          }
        }
       handle_scroll();
        // Add scroll event listener to the window
        window.addEventListener('scroll', handle_scroll);
      },
    };
